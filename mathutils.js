//These are the complex functions that get called

export function add(arg1, arg2) {
    return (arg1 + arg2)
}

export function subtract(arg1, arg2) {
    return (arg1 - arg2)
}

export function multiply(arg1, arg2) {
    return (arg1 * arg2)
}

export function divide(arg1, arg2) {
    return (arg1 / arg2)
}